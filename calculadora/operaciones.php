<?php
class Calcular{
     public $numero1;
     public $numero2;


     function sumar($numero1,$numero2){
        return $total = $numero1 + $numero2;
     }

     function restar($numero1,$numero2){
          return $total = $numero1 - $numero2;
     }

     function multiplicar($numero1,$numero2){
          return $total = $numero1 * $numero2;
     }
     function dividir($numero1,$numero2){
          return $total = $numero1 / $numero2;
     }
}


?>
